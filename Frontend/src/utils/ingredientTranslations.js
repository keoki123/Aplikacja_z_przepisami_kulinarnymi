function normalizeIngredientKey(value) {
  return (value || "").trim().toLowerCase();
}

const normalizedIngredientTranslations = Object.fromEntries(
  Object.entries(ingredientTranslations).map(([key, translation]) => [
    normalizeIngredientKey(key),
    translation
  ])
);

export function translateIngredient(value) {
  if (!value || !value.trim()) {
    return "Nieznany składnik";
  }

  const directMatch = ingredientTranslations[value];
  if (directMatch) {
    return directMatch;
  }

  const normalizedMatch = normalizedIngredientTranslations[normalizeIngredientKey(value)];
  if (normalizedMatch) {
    return normalizedMatch;
  }

  return value;
}