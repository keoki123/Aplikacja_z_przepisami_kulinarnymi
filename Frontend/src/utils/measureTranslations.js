function normalizeMeasure(value) {
  return (value || "").trim().toLowerCase().replace(/\s+/g, " ");
}

const normalizedMeasureTranslations = Object.fromEntries(
  Object.entries(measureTranslations).map(([key, value]) => [
    normalizeMeasure(key),
    value
  ])
);

function escapeRegex(text) {
  return text.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

export function translateMeasurePhrase(value) {
  if (!value || !value.trim()) {
    return "Brak ilości";
  }

  const original = value.trim();
  const normalized = normalizeMeasure(original);

  if (normalizedMeasureTranslations[normalized]) {
    return normalizedMeasureTranslations[normalized];
  }

  let translated = normalized;

  Object.entries(normalizedMeasureTranslations)
    .sort((a, b) => b[0].length - a[0].length)
    .forEach(([english, polish]) => {
      const regex = new RegExp(`\\b${escapeRegex(english)}\\b`, "g");
      translated = translated.replace(regex, polish);
    });

  translated = translated
    .replace(/(\d)(g|kg|ml|l|lb|lbs|oz|cm)\b/g, "$1 $2")
    .replace(/\s+/g, " ")
    .trim();

  return translated || original;
}